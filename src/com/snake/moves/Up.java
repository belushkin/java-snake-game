package com.snake.moves;

public class Up implements Movable{
    public void move() {
        //movedir="UP";
        if (this.y != 0) {
            this.y--;
        } else {
            this.y = areaSize - 2;
        }
        waitInterval(TIMER);
        updatePosition();
        game.setCoord(x, y);
        mvlock = false;
    }
}
